    private void CadastrarProd(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarProd
        // Cadastra os produtos:

        Produto p = new Produto();
        ProdutoDao dao = new ProdutoDao();
        
        p.setReferencia(txtProdRef.getText());
        p.setDescricao(txtProdDesc.getText());
        p.setPreco(Float.parseFloat(txtProdPreco.getText()));
        p.setTipo(cboProdTipo.getSelectedItem().toString());
        dao.create(p);

        txtProdRef.setText("");
        txtProdDesc.setText("");
        txtProdPreco.setText("");

        readJTable();

    }//GEN-LAST:event_CadastrarProd

    private void tbCadProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCadProdMouseClicked
        // TODO add your handling code here:

        if (tbCadProd.getSelectedRow() != -1) {
            txtProdRef.setText(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 1).toString());
            txtProdDesc.setText(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 2).toString());
            txtProdPreco.setText(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 3).toString());
            cboProdTipo.setSelectedItem(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 4));
        }
    }//GEN-LAST:event_tbCadProdMouseClicked

    private void tbCadProdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbCadProdKeyReleased
        // TODO add your handling code here:

        if (tbCadProd.getSelectedRow() != -1) {
            txtProdRef.setText(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 1).toString());
            txtProdDesc.setText(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 2).toString());
            txtProdPreco.setText(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 3).toString());
            cboProdTipo.setSelectedItem(tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 4));
        }
    }//GEN-LAST:event_tbCadProdKeyReleased

    private void AtualizarProd(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarProd
        // Atualiza os Produtos:

        if (tbCadProd.getSelectedRow() != -1) {

            Produto p = new Produto();
            ProdutoDao dao = new ProdutoDao();
            
            p.setId((int)tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 0));
            p.setReferencia(txtProdRef.getText());
            p.setDescricao(txtProdDesc.getText());
            p.setPreco(Float.parseFloat(txtProdPreco.getText()));
            p.setTipo(cboProdTipo.getSelectedItem().toString());
            dao.update(p);

            txtProdRef.setText("");
            txtProdDesc.setText("");
            txtProdPreco.setText("");

            readJTable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para atualizar.");
        }
        
    }//GEN-LAST:event_AtualizarProd

    private void ExcluirProd(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirProd
        // Exclui os Produtos:
        
        if (tbCadProd.getSelectedRow() != -1) {

            Produto p = new Produto();
            ProdutoDao dao = new ProdutoDao();
            
            p.setId((int)tbCadProd.getValueAt(tbCadProd.getSelectedRow(), 0));
            dao.delete(p);

            txtProdRef.setText("");
            txtProdDesc.setText("");
            txtProdPreco.setText("");

            readJTable();
        }
        
        else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }
        
    }//GEN-LAST:event_ExcluirProd


