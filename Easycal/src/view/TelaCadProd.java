    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tbCadProd.getModel();
        modelo.setNumRows(0);

        ProdutoDao pdao = new ProdutoDao();

        for (Produto p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getReferencia(),
                p.getDescricao(),
                p.getPreco(),
                p.getTipo()
            });

        }

    }

